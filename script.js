function activateTab(tabName) {
  const aliases = {
    check: "event",
    toolkit: "event",
    plan: "event",
    practice: "themes"
  };
  tabName = aliases[tabName] || tabName;
  const button = document.querySelector(`[data-tab="${tabName}"]`);
  const panel = document.querySelector(`#${tabName}`);
  if (!panel) return;
  document.querySelectorAll("[data-tab]").forEach((tab) => tab.classList.remove("is-active"));
  document.querySelectorAll(".tab-panel").forEach((item) => item.classList.remove("is-active"));
  if (button) button.classList.add("is-active");
  if (tabName === "themePage") document.querySelector('[data-tab="themes"]')?.classList.add("is-active");
  panel.classList.add("is-active");
  history.replaceState(null, "", `#${tabName}`);
  window.scrollTo({ top: 0, behavior: "auto" });
}

function activateCurrentHash() {
  const initialTab = window.location.hash.replace("#", "") || "home";
  activateTab(initialTab);
}

