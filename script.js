function getRunbookTableSchema(name) {
  return (runbookTableSchemas[currentLanguage] || runbookTableSchemas.nl)[name];
}

function tableRows(name) {
  const schema = getRunbookTableSchema(name);
  const rows = currentEvent().runbook?.[name];
  if (Array.isArray(rows) && rows.length) return rows;
  return schema ? schema.defaults.map((row) => ({ ...row })) : [];
}

function renderTableCell(name, rowIndex, column, value) {
  const base = `data-runbook-table-input="${name}" data-row="${rowIndex}" data-col="${column.key}"`;
  if (column.type === "select") {
    return `<select ${base}>${column.options.map((option) => `<option value="${escapeHTML(option)}" ${value === option ? "selected" : ""}>${escapeHTML(option)}</option>`).join("")}</select>`;
  }
  if (column.type === "checkbox") {
    return `<label class="table-check"><input type="checkbox" ${base} ${value ? "checked" : ""} /><span>${column.label}</span></label>`;
  }
  const type = column.type === "number" ? "number" : "text";
  return `<input type="${type}" ${base} placeholder="${escapeHTML(column.placeholder || "")}" value="${escapeHTML(value || "")}" />`;
}

function renderRunbookTable(name) {
  const schema = getRunbookTableSchema(name);
  const rows = tableRows(name);
  return `
    <div class="runbook-table-wrap">
      <table class="runbook-table">
        <thead>
          <tr>
            ${schema.columns.map((column) => `<th>${escapeHTML(column.label)}</th>`).join("")}
            <th></th>
          </tr>
        </thead>
        <tbody>
          ${rows.map((row, rowIndex) => `
            <tr class="runbook-row" data-runbook-row="${name}" data-row="${rowIndex}">
              ${schema.columns.map((column) => `<td>${renderTableCell(name, rowIndex, column, row[column.key])}</td>`).join("")}
              <td><button type="button" class="table-remove" data-remove-runbook-row="${name}" data-row="${rowIndex}" aria-label="${t("removeRow")}">×</button></td>
            </tr>
          `).join("")}
        </tbody>
      </table>
    </div>
    <button type="button" class="copy-button add-row-button" data-add-runbook-row="${name}">${t("addRow")}</button>
  `;
}

