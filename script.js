function currentEvent() {
  if (!appState.events?.length) {
    appState = normalizeState(appState);
  }
  let event = appState.events.find((item) => item.id === appState.selectedEventId);
  if (!event) {
    event = appState.events[0];
    appState.selectedEventId = event.id;
  }
  return event;
}

