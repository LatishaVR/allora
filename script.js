function emptyState() {
  return { events: [createEmptyEvent()], selectedEventId: null };
}

function normalizeState(state) {
  const hasLegacyData = state && (state.checks || state.profile || state.plan || state.runbook);
  if (!state || (!Array.isArray(state.events) && !hasLegacyData)) {
    const fresh = emptyState();
    fresh.selectedEventId = fresh.events[0].id;
    return fresh;
  }
  if (Array.isArray(state.events) && state.events.length) {
    const events = state.events.map(normalizeEvent);
    return { events, selectedEventId: state.selectedEventId || events[0].id };
  }
  const migrated = normalizeEvent({
    id: "event-legacy",
    name: state.profile?.eventName || "",
    checks: state.checks || {},
    profile: state.profile || {},
    plan: state.plan || {},
    runbook: state.runbook || {},
    actions: state.actions || []
  });
  return { events: [migrated], selectedEventId: migrated.id };
}

function createEmptyEvent(name = "", organizerTools = {}) {
  const cleanName = (name || "").trim();
  return normalizeEvent({
    id: `event-${Date.now()}-${Math.random().toString(16).slice(2)}`,
    name: cleanName,
    checks: {},
    profile: cleanName ? { eventName: cleanName } : {},
    plan: {},
    runbook: {},
    organizerTools,
    actions: [],
    needsName: !cleanName
  });
}

function normalizeEvent(event) {
  const profile = event.profile || {};
  const storedName = (event.name || profile.eventName || "").trim();
  const defaultNames = [copy.nl.newEventName, copy.en.newEventName, copy.fr.newEventName];
  const needsName = Boolean(event.needsName) || !storedName || defaultNames.includes(storedName);
  const organizerTools = event.organizerTools || {};
  if (!Array.isArray(organizerTools.priorities) && organizerTools.priority) {
    organizerTools.priorities = [organizerTools.priority];
  }
  return {
    id: event.id || `event-${Date.now()}-${Math.random().toString(16).slice(2)}`,
    name: needsName ? "" : storedName,
    checks: event.checks || {},
    profile: needsName ? { ...profile, eventName: "" } : profile,
    plan: event.plan || {},
    runbook: event.runbook || {},
    organizerTools,
    actions: Array.isArray(event.actions) ? event.actions : [],
    needsName
  };
}

