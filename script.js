function caseThemes(item) {
  return item.themes || [item.theme];
}

function visibleCaseThemes(item) {
  const hiddenThemes = guideOnlyCaseThemes();
  return caseThemes(item).filter((theme) => !hiddenThemes.includes(theme));
}

function caseMatchesTheme(item, theme) {
  if (theme === "all") return true;
  if (caseThemes(item).includes(theme)) return true;
  if (item.theme === theme) return true;
  if (theme === "Evaluatie en inclusieplan" && item.theme === "Evaluatie") return true;
  return item.theme.includes(theme) || theme.includes(item.theme);
}

