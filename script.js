document.addEventListener("click", async (event) => {
  const adviceTarget = event.target.closest("[data-open-event-type-advice]");
  if (adviceTarget) {
    openEventTypeAdvice(adviceTarget.dataset.openEventTypeAdvice);
    return;
  }

  if (event.target.closest("[data-close-advice]")) {
    closeEventTypeAdvice();
    return;
  }

  const priorityTarget = event.target.closest("[data-priority-key]");
  if (priorityTarget) {
    togglePriority(priorityTarget.dataset.priorityKey);
    persistState();
    renderOrganizerTools();
    return;
  }

  const createPriorityTarget = event.target.closest("[data-create-priority-key]");
  if (createPriorityTarget) {
    const key = createPriorityTarget.dataset.createPriorityKey;
    if (isCreatingEvent && !eventNeedsName()) {
      draftPriorityKeys = draftPriorityKeys.includes(key)
        ? draftPriorityKeys.filter((item) => item !== key)
        : [...draftPriorityKeys, key];
    } else {
      togglePriority(key);
      persistState();
    }
    renderEventPriorityChoices();
    renderOrganizerTools();
    return;
  }

  const copyToolTarget = event.target.closest("[data-copy-tool]");
  if (copyToolTarget) {
    await navigator.clipboard.writeText(copyToolTarget.dataset.copyTool);
    showToast(t("copied"));
    return;
  }

  const addActionTarget = event.target.closest("[data-add-action]");
  if (addActionTarget) {
    addActionToCurrentEvent(addActionTarget.dataset.addAction, addActionTarget.dataset.actionSource || "");
    return;
  }

  const removeActionTarget = event.target.closest("[data-remove-action]");
  if (removeActionTarget) {
    const index = Number(removeActionTarget.dataset.removeAction);
    const activeEvent = currentEvent();
    activeEvent.actions = (activeEvent.actions || []).filter((_, actionIndex) => actionIndex !== index);
    persistState();
    renderSavedActions();
    return;
  }

  const themeTarget = event.target.closest("[data-theme-index]");
  if (themeTarget) {
    selectedThemeIndex = Number(themeTarget.dataset.themeIndex);
    renderLearn();
    renderThemePage();
    activateTab("themePage");
    window.scrollTo({ top: 0, behavior: "smooth" });
    return;
  }

  const themeBackTarget = event.target.closest("[data-theme-back]");
  if (themeBackTarget) {
    activateTab("themes");
    window.scrollTo({ top: 0, behavior: "smooth" });
    return;
  }

  const openThemeCasesTarget = event.target.closest("[data-open-theme-cases]");
  if (openThemeCasesTarget) {
    const themeTarget = openThemeCasesTarget.dataset.openThemeCases;
    selectedCaseTheme = guideOnlyCaseThemes().includes(themeTarget) ? "all" : themeTarget;
    renderCases();
    activateTab("cases");
    window.scrollTo({ top: 0, behavior: "smooth" });
    return;
  }

  const caseThemeTarget = event.target.closest("[data-case-theme]");
  if (caseThemeTarget) {
    selectedCaseTheme = caseThemeTarget.dataset.caseTheme;
    renderCases();
    return;
  }

  const addRunbookTarget = event.target.closest("[data-add-runbook-row]");
  if (addRunbookTarget) {
    event.preventDefault();
    addRunbookRow(addRunbookTarget.dataset.addRunbookRow);
    return;
  }

  const removeRunbookTarget = event.target.closest("[data-remove-runbook-row]");
  if (removeRunbookTarget) {
    event.preventDefault();
    removeRunbookRow(removeRunbookTarget.dataset.removeRunbookRow, Number(removeRunbookTarget.dataset.row));
    return;
  }

  const tabTarget = event.target.closest("[data-open-tab]");
  if (tabTarget) {
    event.preventDefault();
    activateTab(tabTarget.dataset.openTab);
    if (tabTarget.classList.contains("brand")) {
      window.scrollTo({ top: 0, behavior: "smooth" });
    }
  }

  const scenarioChoice = event.target.dataset.scenarioChoice;
  if (scenarioChoice !== undefined) {
    renderScenario(Number(scenarioChoice));
  }

  if (event.target.id === "nextScenario") {
    currentScenario = (currentScenario + 1) % currentContent().scenarios.length;
    renderScenario();
  }

  if (event.target.id === "previousScenario") {
    currentScenario = (currentScenario - 1 + currentContent().scenarios.length) % currentContent().scenarios.length;
    renderScenario();
  }
});

