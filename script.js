function selectedPriority() {
  const key = currentEvent().organizerTools?.priority;
  const toolkit = currentOrganizerToolkit();
  return toolkit.priorities.find((item) => item.key === key) || toolkit.priorities[0];
}

function selectedPriorityKeys(event = currentEvent()) {
  const tools = event.organizerTools || {};
  if (Array.isArray(tools.priorities)) return tools.priorities;
  return tools.priority ? [tools.priority] : [];
}

function selectedPriorities(event = currentEvent()) {
  const toolkit = currentOrganizerToolkit();
  const keys = selectedPriorityKeys(event);
  return toolkit.priorities.filter((item) => keys.includes(item.key));
}

function togglePriority(key, event = currentEvent()) {
  const tools = event.organizerTools || {};
  const currentKeys = selectedPriorityKeys(event);
  const nextKeys = currentKeys.includes(key)
    ? currentKeys.filter((item) => item !== key)
    : [...currentKeys, key];
  event.organizerTools = {
    ...tools,
    priorities: nextKeys,
    priority: nextKeys[0] || ""
  };
}

