function runbookFieldText(name, value) {
  const schema = getRunbookTableSchema(name);
  if (!schema) return value || "";
  const rows = Array.isArray(value) ? value : [];
  return rows.map((row) => schema.columns
    .map((column) => `${column.label}: ${column.type === "checkbox" ? (row[column.key] ? "Ja" : "Nee") : row[column.key] || ""}`)
    .join(" | "))
    .join("\n");
}

function hasFilledValue(value) {
  if (typeof value === "boolean") return value;
  if (typeof value === "number") return true;
  return Boolean(String(value || "").trim());
}

function rowHasFilledValue(row, schema) {
  return schema.columns.some((column) => hasFilledValue(row?.[column.key]));
}

function filledFieldPairs(fields, values) {
  return fields
    .map(([name, label]) => [label, values[name]])
    .filter(([, value]) => hasFilledValue(value));
}

function textSection(title, body) {
  return body ? `${title}\n${body}` : "";
}

function htmlSection(title, html) {
  return html ? `<h2>${escapeHTML(title)}</h2>${html}` : "";
}

function runbookText() {
  const profile = {
    ...Object.fromEntries(new FormData(eventProfileForm).entries()),
    eventName: currentEvent().name || currentEvent().profile?.eventName || ""
  };
  saveOrganizerTools();
  saveRunbook();
  const event = currentEvent();
  const runbook = event.runbook || {};
  const tools = event.organizerTools || {};
  const toolkit = currentOrganizerToolkit();
  const plan = {
    ...Object.fromEntries(new FormData(planForm).entries()),
    eventName: currentEvent().name || currentEvent().profile?.eventName || ""
  };
  const checks = getStoredChecks();
  const profileText = filledFieldPairs(currentContent().eventFields, profile)
    .map(([label, value]) => `${label}: ${value}`)
    .join("\n");
  const runbookBlock = getRunbookFields()
    .map(([name, label]) => {
      const schema = getRunbookTableSchema(name);
      if (schema) {
        const rows = (Array.isArray(runbook[name]) ? runbook[name] : []).filter((row) => rowHasFilledValue(row, schema));
        if (!rows.length) return "";
        return `${label}:\n${runbookFieldText(name, rows)}`;
      }
      const value = runbook[name];
      return hasFilledValue(value) ? `${label}:\n${value}` : "";
    })
    .filter(Boolean)
    .join("\n\n");
  const planTextBlock = filledFieldPairs(currentContent().planFields, plan)
    .map(([label, value]) => `${label}: ${value}`)
    .join("\n");
  const priorities = selectedPriorities(event);
  const eventType = toolkit.eventTypes.find((item) => item.key === tools.eventType);
  const accessText = toolkit.accessFields
    .map(([name, label]) => [label, tools.access?.[name]])
    .filter(([, value]) => hasFilledValue(value))
    .map(([label, value]) => `${label}: ${value}`)
    .join("\n");
  const toolkitBlock = [
    eventType ? `Eventvorm: ${eventType.title}` : "",
    priorities.length ? `Prioriteiten:\n${priorities.map((priority) => `- ${priority.title}\n${priority.actions.map((action) => `  - ${action}`).join("\n")}`).join("\n")}` : "",
    textSection("Toegankelijkheidsfiche:", accessText)
  ].filter(Boolean).join("\n\n");
  const checklistBlock = currentContent().checklist.map((group) => {
    const items = group.items.map((item, itemIndex) => {
      const key = `${group.id}-${itemIndex}`;
      return `[${checks[key] ? "x" : " "}] ${item}`;
    }).filter((item) => item.startsWith("[x]")).join("\n");
    if (!items) return "";
    return `${group.title}\n${items}`;
  }).filter(Boolean).join("\n\n");
  const actionsBlock = (event.actions || []).map((action) => `- ${action.text}${action.source ? ` (${action.source})` : ""}`).join("\n");
  return [
    "ALLORA DRAAIBOEK",
    textSection("EVENTFICHE", profileText),
    textSection("STARTKEUZES", toolkitBlock),
    textSection("DRAAIBOEK", runbookBlock),
    textSection("INCLUSIEPLAN", planTextBlock),
    textSection("OPGESLAGEN ACTIES", actionsBlock),
    textSection("CHECKLISTS PER THEMA", checklistBlock)
  ].filter(Boolean).join("\n\n");
}

